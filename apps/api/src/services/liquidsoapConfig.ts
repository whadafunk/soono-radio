import { readFile, writeFile } from 'fs/promises';
import { join } from 'path';
import { parseStringPromise } from 'xml2js';
import { LiquidsoapConfig, LiquidsoapConfigSchema } from '@radio/shared';

const CONFIG_PATH =
  process.env.LIQUIDSOAP_CONFIG ||
  join(process.cwd(), '..', '..', 'liquidsoap', 'supervisor.json');

const SCRIPT_PATH =
  process.env.LIQUIDSOAP_SCRIPT ||
  join(process.cwd(), '..', '..', 'liquidsoap', 'mix-engine.liq');

const ICECAST_CONFIG_PATH =
  process.env.ICECAST_CONFIG ||
  join(process.cwd(), '..', '..', 'icecast', 'icecast.xml');

const DEFAULT_CONFIG: LiquidsoapConfig = LiquidsoapConfigSchema.parse({
  output: {},
  harbor: { password: 'changeme' },
  automation: {},
  crossfade: {},
});

export async function readLiquidsoapConfig(): Promise<LiquidsoapConfig> {
  try {
    const json = await readFile(CONFIG_PATH, 'utf-8');
    const parsed = JSON.parse(json);
    return LiquidsoapConfigSchema.parse(parsed);
  } catch (err: any) {
    if (err.code === 'ENOENT') {
      return DEFAULT_CONFIG;
    }
    throw err;
  }
}

export async function writeLiquidsoapConfig(config: LiquidsoapConfig): Promise<void> {
  await writeFile(CONFIG_PATH, JSON.stringify(config, null, 2) + '\n', 'utf-8');
  await generateRadioLiq(config);
}

async function readIcecastSourcePassword(): Promise<string> {
  const xml = await readFile(ICECAST_CONFIG_PATH, 'utf-8');
  const parsed = await parseStringPromise(xml, { trim: true });
  const password = parsed?.icecast?.authentication?.[0]?.['source-password']?.[0];
  if (!password) {
    throw new Error('Could not read source-password from icecast.xml');
  }
  return password;
}

export async function generateRadioLiq(config: LiquidsoapConfig): Promise<string> {
  const sourcePassword = await readIcecastSourcePassword();
  const script = renderScript(config, sourcePassword);
  await writeFile(SCRIPT_PATH, script, 'utf-8');
  return script;
}

function renderScript(config: LiquidsoapConfig, icecastSourcePassword: string): string {
  const lines: string[] = [];

  lines.push('# Generated by Radio app — DO NOT EDIT (use Settings → Mix Engine, or Raw Script for advanced).');
  lines.push('');
  lines.push('settings.server.telnet.set(true)');
  lines.push('settings.server.telnet.bind_addr.set("0.0.0.0")');
  lines.push('settings.server.telnet.port.set(1234)');
  lines.push('');

  lines.push('# Automation source');
  if (config.automation.mode === 'silence') {
    lines.push('auto = blank(id="automation")');
  } else {
    lines.push(
      `auto = playlist(id="automation", reload_mode="watch", "${escapeStr(config.automation.playlist_dir)}")`,
    );
  }
  lines.push('');

  if (config.harbor.enabled) {
    lines.push('# Live source via Harbor (Icecast source-protocol input)');
    lines.push('live = input.harbor(');
    lines.push(`  id="live",`);
    lines.push(`  "${escapeStr(config.harbor.mount_name)}",`);
    lines.push(`  port=${config.harbor.port},`);
    lines.push(`  password="${escapeStr(config.harbor.password)}"`);
    lines.push(')');
    lines.push('');
  }

  lines.push('# Source priority — live takes over when present, automation otherwise');
  if (config.harbor.enabled) {
    lines.push('radio = fallback(track_sensitive=false, [live, auto])');
  } else {
    lines.push('radio = fallback(track_sensitive=false, [auto])');
  }
  if (config.crossfade.duration_seconds > 0) {
    lines.push(`radio = cross(duration=${config.crossfade.duration_seconds.toFixed(1)}, radio)`);
  }
  lines.push('');

  lines.push('# Output to Icecast (source password resolved from icecast.xml at generation time)');
  lines.push('output.icecast(');
  lines.push('  %mp3(bitrate=128, samplerate=44100, stereo=true),');
  lines.push(`  host="${escapeStr(config.output.icecast_host)}",`);
  lines.push(`  port=${config.output.icecast_port},`);
  lines.push('  user="source",');
  lines.push(`  password="${escapeStr(icecastSourcePassword)}",`);
  lines.push(`  mount="${escapeStr(config.output.icecast_mount)}",`);
  lines.push('  radio');
  lines.push(')');
  lines.push('');

  return lines.join('\n');
}

function escapeStr(value: string): string {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

export async function readRadioLiq(): Promise<string> {
  return readFile(SCRIPT_PATH, 'utf-8');
}

export async function writeRadioLiq(content: string): Promise<void> {
  await writeFile(SCRIPT_PATH, content, 'utf-8');
}
